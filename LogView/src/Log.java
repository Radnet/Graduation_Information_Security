
public class Log {
	
	public String text;
	public String time;
	
	public Log()
	{
		text = "";
		time = "";
	}
	
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}

}
